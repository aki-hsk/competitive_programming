#include<bits/stdc++.h>
using namespace std;

int main() {
    //intでキャストすることで内部での数値を確認する
    cout << (int)'A' << endl;
    cout << (int)'B' << endl;
    cout << (int)'Z' << endl;
    //charでキャストすることで数値がどの文字かを確認
    cout << (char)65 << endl;
    cout << (char)66 << endl;
    cout << (char)90 << endl;
}